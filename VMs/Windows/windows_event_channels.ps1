# =============================================================================
# THEORY BLOCK 1: THE WINDOWS EVENT LOG ARCHITECTURE
# =============================================================================
# Windows does not have one log file. It has a hierarchy of named "channels"
# managed by the Windows Event Log service (WEvtSvc, svchost.exe).
#
# Each channel is registered by a "provider" (publisher). The provider declares
# its channel in a manifest (.man file) and registers it with Windows. The
# Event Log service then routes events from that provider into the correct
# channel.
#
# Channel types:
#   Classic    : Security, System, Application
#                These predate Vista. They use a simpler event format.
#   Operational: Microsoft-Windows-PowerShell/Operational
#                These use the modern ETW (Event Tracing for Windows) pipeline.
#
# The four channels and their purpose:
#
#   SECURITY
#     Owner  : Windows Security Reference Monitor (SRM) in the kernel
#              + LSASS (Local Security Authority Subsystem Service)
#     Purpose: Records authentication events, privilege use, account changes,
#              object access, policy changes.
#     Key    : Events here are generated by the KERNEL, not by apps. You need
#              "audit policy" configured for events to be generated at all.
#     Linux  : Closest equivalent is /var/log/auth.log + auditd
#
#   SYSTEM
#     Owner  : Windows kernel, device drivers, core OS services
#     Purpose: OS-level events — service start/stop, hardware failures,
#              driver loads, boot events.
#     Key    : Event ID 7045 (new service installed) lives here — this is a
#              common persistence and lateral movement indicator.
#     Linux  : Closest equivalent is /var/log/syslog or journald
#
#   APPLICATION
#     Owner  : User-mode applications (IIS, SQL Server, antivirus, etc.)
#     Purpose: Application-specific events. Highly variable — every app
#              decides what it logs here.
#     Key    : Less structured than Security. Useful for application errors
#              and some product-specific security events (IIS 401s, etc.).
#     Linux  : Closest equivalent is /var/log/application-name.log
#
#   MICROSOFT-WINDOWS-POWERSHELL/OPERATIONAL
#     Owner  : PowerShell engine
#     Purpose: Records script execution, module loads, remoting, errors.
#              This is the primary channel for PowerShell-based attack detection.
#     Key    : NOT enabled by default. Requires registry configuration.
#              Requires separate Fleet integration entry or custom channel config.
#     Linux  : No direct equivalent (bash doesn't have built-in structured
#              logging — you'd use auditd with execve rules instead)
# =============================================================================


# =============================================================================
# THEORY BLOCK 2: AUDIT POLICY — WHY EVENTS DON'T APPEAR WITHOUT IT
# =============================================================================
# This is a critical concept that many analysts miss.
#
# The Security event log is NOT like a normal application log. Events are only
# generated when:
#   1. The Windows SRM (kernel) decides an auditable event occurred, AND
#   2. The relevant "audit subcategory" is ENABLED in audit policy
#
# Without audit policy, the kernel simply doesn't generate the events.
# You can have Elastic Agent perfectly configured to read the Security channel
# and still get nothing — because there's nothing being written to it.
#
# Two layers of audit policy:
#
#   Basic audit policy (9 categories):
#     Set via: Local Security Policy > Audit Policy
#     Or:      auditpol /set /category:"..."
#     Legacy approach — predates Vista
#
#   Advanced audit policy (53 subcategories):
#     Set via: Local Security Policy > Advanced Audit Policy
#     Or:      auditpol /set /subcategory:"..."
#     Preferred — more granular, avoids enabling entire categories
#
# We always use the ADVANCED (subcategory) approach in detection engineering
# because the basic categories enable too much at once.
#
# Key subcategories for detection:
#   Logon             -> 4624 (success), 4625 (failure)
#   Logoff            -> 4634
#   Process Creation  -> 4688
#   User Account Mgmt -> 4720, 4722, 4724, 4726, 4728, 4738, 4740
#   Security Group Mgmt -> 4728, 4732, 4756
#   Special Logon     -> 4672 (admin token used at logon)
#   Sensitive Privilege Use -> 4673, 4674
# =============================================================================


# =============================================================================
# THEORY BLOCK 3: THE SIX EVENT IDs YOU MUST KNOW
# =============================================================================
#
# --- SECURITY CHANNEL ---
#
# Event ID 4624 — Account Logon Success
#   Generated when: Any account successfully authenticates
#   Key field: LogonType (tells you HOW the logon happened)
#
#   LogonType values:
#     2  = Interactive      (keyboard at console — normal local login)
#     3  = Network          (SMB, net use, file share — lateral movement source)
#     4  = Batch            (scheduled task ran — check for persistence)
#     5  = Service          (service account starting — service installation)
#     7  = Unlock           (workstation unlock — normal)
#     10 = RemoteInteractive (RDP session — high-value for RDP tracking)
#     11 = CachedInteractive (cached domain creds — offline domain logon)
#
#   Detection patterns:
#     - Workstation-to-workstation Type 3 logon = lateral movement
#     - Type 10 (RDP) from unexpected IP = external RDP access
#     - Type 5 at odd hours = suspicious service install
#     - Many 4624s from one source IP = pass-the-hash or brute force success
#
# Event ID 4625 — Account Logon Failure
#   Generated when: Authentication fails
#   Key field: SubStatus (tells you WHY it failed)
#
#   SubStatus values:
#     0xC000006A = Wrong password (account EXISTS — useful for spray detection)
#     0xC0000064 = Username not found (account DOESN'T exist — enumeration)
#     0xC000006F = Logon outside allowed hours (account policy violation)
#     0xC000006D = Generic auth failure (bad credentials)
#     0xC0000234 = Account locked out
#
#   Detection patterns:
#     - Same source IP, many 4625 in short time = brute force
#     - 0xC0000064 SubStatus = username enumeration
#     - Many 4625 across many usernames from one IP = password spray
#       (spray = one password tried against many accounts to avoid lockout)
#
# Event ID 4688 — Process Create (native Windows)
#   Generated when: Any new process spawns
#   Key fields: NewProcessName, CommandLine, ParentProcessName, SubjectUserName
#
#   How it differs from Sysmon Event ID 1:
#     4688: No hashes, command line requires registry key, native (no install)
#     Sysmon 1: SHA256 hash, command line always present, requires Sysmon install
#   With Sysmon installed, you have BOTH — 4688 for compatibility with rules
#   written against native Windows logging, Sysmon 1 for richer data.
#
#   Detection patterns:
#     - cmd.exe or powershell.exe spawned by Office apps (macro execution)
#     - wscript.exe or cscript.exe running .vbs or .js files
#     - powershell.exe with -enc flag (base64 encoded command = obfuscation)
#     - net.exe or net1.exe commands (user/group enumeration or modification)
#     - whoami.exe immediately after any process = attacker checking context
#
# Event ID 4720 — User Account Created
#   Generated when: A local user account is created
#   Key fields: SubjectUserName (who created it), TargetUserName (new account)
#
#   Detection patterns:
#     - Account created outside business hours = backdoor creation
#     - 4720 followed quickly by 4728 (added to security group) = privilege escalation
#     - Account with suspicious name (numbers, random chars) = attacker persistence
#     - Created by a non-admin account = potential privilege escalation
#
# --- POWERSHELL OPERATIONAL CHANNEL ---
#
# Event ID 4103 — Module Logging
#   Generated when: Any PowerShell pipeline executes (requires Module Logging enabled)
#   Records: The full command AND its output
#   Volume: HIGH — logs every pipeline execution
#   Value: Captures the OUTPUT of commands (useful for data exfil detection)
#
# Event ID 4104 — Script Block Logging
#   Generated when: PowerShell compiles a script block to execute
#   Records: The FULL TEXT of the script block, post-deobfuscation
#   Volume: MEDIUM — one event per script block
#   Value: Captures obfuscated scripts in their decoded form
#
#   This is the most important PowerShell detection event.
#   Attackers use base64 encoding, string concatenation, char arrays, etc.
#   to obfuscate PowerShell. 4104 captures the code AFTER Windows decodes it.
#
#   Example: Attacker runs:
#     powershell -enc SQBuAHYAbwBrAGUALQBNAGkAbQBpAGsAYQB0AHoA
#   Without 4104: You see an encoded blob
#   With 4104: You see: Invoke-Mimikatz
#
#   Detection patterns:
#     - ScriptBlockText contains: Invoke-Mimikatz, Invoke-Empire, PowerSploit
#     - ScriptBlockText contains: DownloadString, IEX, Invoke-Expression
#     - ScriptBlockText contains: -enc or FromBase64String (re-encoding in script)
#     - ScriptBlockText contains: net.webclient, WebRequest (download cradles)
# =============================================================================


