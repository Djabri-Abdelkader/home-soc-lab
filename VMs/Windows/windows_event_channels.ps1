# Run as Administrator

# View current state of all audit subcategories
auditpol /get /category:*

# --- Logon / Logoff (generates 4624, 4625, 4634, 4647) ---
# Both success and failure for Logon (you want to catch both real logins
# AND failed attempts for brute force detection)
auditpol /set /subcategory:"Logon" /success:enable /failure:enable
auditpol /set /subcategory:"Logoff" /success:enable /failure:enable

# Special Logon (generates 4672 — admin token used)
# This fires when an account with admin privileges logs in.
# Useful for tracking privileged account usage.
auditpol /set /subcategory:"Special Logon" /success:enable /failure:enable

# --- Process Tracking (generates 4688, 4689) ---
# 4688 = process created, 4689 = process exited
# Enable both — exit events let you calculate process duration
auditpol /set /subcategory:"Process Creation" /success:enable /failure:enable
auditpol /set /subcategory:"Process Termination" /success:enable /failure:enable

# --- Account Management (generates 4720, 4722, 4724, 4726, 4728, 4738, 4740) ---
# User Account Management: create/delete/modify user accounts
auditpol /set /subcategory:"User Account Management" /success:enable /failure:enable
# Security Group Management: add/remove from security groups (4728, 4732, 4756)
auditpol /set /subcategory:"Security Group Management" /success:enable /failure:enable

