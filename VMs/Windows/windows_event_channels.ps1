# =============================================================================
# INTERVIEW PREP — KEY CONCEPTS TO BE ABLE TO EXPLAIN
# =============================================================================
#
# Q: Why do Security channel events require audit policy?
# A: The Security channel is written by the Windows kernel SRM, not by a
#    user-mode application. The kernel only generates audit events when the
#    relevant subcategory is enabled in audit policy. Without it, nothing is
#    written. auditpol.exe or Group Policy configures which subcategories are
#    active. In enterprise environments, this is set via GPO and pushed to all
#    machines — if a machine's audit policy is misconfigured, you get a blind spot.
#
# Q: What's the difference between basic and advanced audit policy?
# A: Basic audit policy has 9 broad categories (Logon/Logoff, Object Access,
#    etc.). Advanced audit policy has 53 subcategories within those categories.
#    Always use advanced (subcategory) audit policy — it's more granular and
#    avoids enabling too much at once. You want "Logon" specifically, not the
#    entire "Logon/Logoff" category, which also includes noisy events.
#
# Q: Why is 4625 Sub Status more useful than just the failure count?
# A: Sub Status tells you WHY auth failed. 0xC0000064 means the username
#    doesn't exist — if you see this with many different usernames from one IP,
#    that's username enumeration. 0xC000006A means bad password on a valid
#    account — that's password spraying or brute force. Same event ID,
#    completely different attacker behavior.
#
# Q: What is PowerShell Script Block Logging and why does it matter?
# A: Event ID 4104 captures the full text of every PowerShell script block
#    AFTER Windows deobfuscates it. Attackers heavily obfuscate PS code with
#    base64 encoding, string concatenation, char arrays, etc. Without 4104,
#    you see an opaque blob. With 4104, Windows has already decoded it for
#    execution, and you see the actual malicious commands.
#
# Q: What's LogonType 3 and why do you hunt it for lateral movement?
# A: LogonType 3 is a network logon — authentication over the network (SMB,
#    net use, WMI, etc.). Legitimate Type 3 logons happen from users to file
#    servers. Suspicious Type 3: a workstation authenticating to another
#    workstation — workstations don't normally do this. If you see W1 -> W2
#    Type 3, that's classic lateral movement (PsExec, WMI, etc.).
#
# Q: How does 4688 compare to Sysmon Event ID 1?
# A: Both log process creation. 4688 is native (no install needed), always
#    present, but requires audit policy and a registry key to get command lines,
#    and provides no file hashes. Sysmon ID 1 requires Sysmon installation,
#    but gives you SHA256 hashes, always-on command lines, and ProcessGUID for
#    correlation. In a real SOC, you'd have both — some detection rules are
#    written against 4688, others against Sysmon data.


