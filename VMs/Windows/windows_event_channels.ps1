# =============================================================================
# STEP 2: ENABLE COMMAND LINE LOGGING IN 4688
# =============================================================================
# By default, 4688 shows the process name but NOT the command line arguments.
# This registry key enables full command line capture.
# Without this, 4688 is far less useful for detection.

reg add "HKLM\SOFTWARE\Microsoft\Windows\CurrentVersion\Policies\System\Audit" `
    /v ProcessCreationIncludeCmdLine_Enabled `
    /t REG_DWORD /d 1 /f

# Verify
$regVal = Get-ItemProperty `
    -Path "HKLM:\SOFTWARE\Microsoft\Windows\CurrentVersion\Policies\System\Audit" `
    -Name "ProcessCreationIncludeCmdLine_Enabled" `
    -ErrorAction SilentlyContinue
Write-Host "ProcessCreationIncludeCmdLine_Enabled = $($regVal.ProcessCreationIncludeCmdLine_Enabled)"
# Expected: 1


# =============================================================================
# STEP 3: ENABLE POWERSHELL SCRIPT BLOCK LOGGING (Event ID 4104)
# =============================================================================
# This is NOT enabled by default. Requires registry configuration.
# These keys may not exist — we create them if absent.

# Script Block Logging path
$sbPath = "HKLM:\SOFTWARE\Policies\Microsoft\Windows\PowerShell\ScriptBlockLogging"
if (-not (Test-Path $sbPath)) {
    New-Item -Path $sbPath -Force | Out-Null
    Write-Host "Created ScriptBlockLogging registry key"
}

# EnableScriptBlockLogging = 1 -> log all script blocks
Set-ItemProperty -Path $sbPath -Name "EnableScriptBlockLogging" -Value 1 -Type DWord

# EnableScriptBlockInvocationLogging = 1 -> also log start/stop of each block
# More verbose — log both the block text AND when each block starts/stops
Set-ItemProperty -Path $sbPath -Name "EnableScriptBlockInvocationLogging" -Value 1 -Type DWord

Write-Host "Script Block Logging (4104) enabled."


# =============================================================================
# STEP 4: ENABLE POWERSHELL MODULE LOGGING (Event ID 4103)
# =============================================================================
# Module logging captures the full pipeline input/output.
# It logs every call to every PowerShell cmdlet.
# More verbose than 4104 but also captures output, not just input.

# Module logging path
$mlPath = "HKLM:\SOFTWARE\Policies\Microsoft\Windows\PowerShell\ModuleLogging"
if (-not (Test-Path $mlPath)) {
    New-Item -Path $mlPath -Force | Out-Null
    Write-Host "Created ModuleLogging registry key"
}

# EnableModuleLogging = 1
Set-ItemProperty -Path $mlPath -Name "EnableModuleLogging" -Value 1 -Type DWord

# Specify which modules to log — "*" means ALL modules
# This is a subkey containing module name entries
$mlModPath = "$mlPath\ModuleNames"
if (-not (Test-Path $mlModPath)) {
    New-Item -Path $mlModPath -Force | Out-Null
}
# The wildcard "*" catches everything — in a real env you might limit this
# to Microsoft.PowerShell.* and specific known-attack modules
Set-ItemProperty -Path $mlModPath -Name "*" -Value "*" -Type String

Write-Host "Module Logging (4103) enabled for all modules."


